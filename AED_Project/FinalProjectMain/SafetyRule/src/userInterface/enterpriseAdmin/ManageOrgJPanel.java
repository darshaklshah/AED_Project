    @SuppressWarnings("unchecked")
    
    private void popTableData(){
        int rowCount = viewOrgJTable.getRowCount();
        for(int i=rowCount-1; i>=0; i--){
            ((DefaultTableModel)viewOrgJTable.getModel()).removeRow(i);
        }
        for(Organization org:enterprise.getOrganizationDirectory().getOrganizationList()){
            Object row[] = new Object[2];
            row[0] = org;
            row[1] = org.getType();
            
            ((DefaultTableModel)viewOrgJTable.getModel()).addRow(row);  
        }
    }
    
    private void popComboBox(){
        typeJComboBox.removeAllItems();
        if(enterprise instanceof Hospital){
            typeJComboBox.addItem(Hospital.HOSREPORTER);
        }else if(enterprise instanceof PSO){
            typeJComboBox.addItem(PSO.PSOVALIDATION);
        }else if(enterprise instanceof Individual){
            typeJComboBox.addItem(Individual.INDREPORTER);
        }else if(enterprise instanceof Government){
            typeJComboBox.addItem(Government.GOVANALYST);
        }
    }
    
