    private void createOrgJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createOrgJButtonActionPerformed
        // TODO add your handling code here:
        String orgType = (String)typeJComboBox.getSelectedItem();
        if(nameJTextField.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Input organization name");
            return;
        }
        Organization org = enterprise.getOrganizationDirectory().createOrganization(orgType);
        org.setName(nameJTextField.getText());
        org.setType(orgType);
        if(enterprise instanceof Government){
            ((GovernmentAnalyst)org).setGov((Government)enterprise);
        }
        popTableData();
        nameJTextField.setText("");
    }//GEN-LAST:event_createOrgJButtonActionPerformed

