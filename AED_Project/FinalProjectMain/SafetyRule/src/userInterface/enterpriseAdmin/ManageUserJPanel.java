    private void popTableData(){
        DefaultTableModel model = (DefaultTableModel) userJTable.getModel();

        model.setRowCount(0);

        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
            for (UserAccount ua : organization.getUserAccountDirectory().getUserAccountList()) {
                Object row[] = new Object[2];
                row[0] = ua;
                row[1] = ua.getRole();
                ((DefaultTableModel) userJTable.getModel()).addRow(row);
            }
        }
    }
    
    private void popOrgComboBox(){
        orgJComboBox.removeAllItems();

        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
            orgJComboBox.addItem(organization);
        }
    }
    
    private void popPersonComboBox(Organization org){
        personJComboBox.removeAllItems();
        
        for (Person person : org.getPersonDirectory().getPersonList()){
            personJComboBox.addItem(person);
        }
    }
    
    private void popRoleComboBox(Organization org){
        roleJComboBox.removeAllItems();
        for (Role role : org.getSupportedRole()){
            roleJComboBox.addItem(role);
        }
    }
    
