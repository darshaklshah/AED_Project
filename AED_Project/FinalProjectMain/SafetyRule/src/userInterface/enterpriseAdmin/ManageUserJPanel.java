    private void createJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createJButtonActionPerformed
        // TODO add your handling code here:
        String userName = userNameJTextField.getText();
        String password = passwordJTextField.getText();
        if(userName.trim().isEmpty()||password.trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Input info");
            return;
        }
        if(system.checkAllUnique(userName)){
            Organization organization = (Organization) orgJComboBox.getSelectedItem();
            Person person = (Person) personJComboBox.getSelectedItem();
            Role role = (Role) roleJComboBox.getSelectedItem();
            organization.getUserAccountDirectory().createUserAccount(userName, password, person, role);
        }else{
            JOptionPane.showMessageDialog(null, "User name is occupied");
        }
        userNameJTextField.setText("");
        passwordJTextField.setText("");
        popTableData();
    }//GEN-LAST:event_createJButtonActionPerformed

    private void deleteJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteJButtonActionPerformed
        // TODO add your handling code here:
        if(userJTable.getSelectedRowCount() == 0){
            return;
        }
        int selectedRow = userJTable.getSelectedRow();
        UserAccount s = (UserAccount)userJTable.getValueAt(selectedRow, 0);
        Organization selectOrg = null;
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
            for (UserAccount ua : organization.getUserAccountDirectory().getUserAccountList()) {
               if(ua.equals(s)){
                   selectOrg = organization;
                   break;
               }
            }
        }
        if(selectOrg!=null){
            selectOrg.getUserAccountDirectory().deleteUserAccount(s);
        }else{
            return;
        }
        
        popTableData();
    }//GEN-LAST:event_deleteJButtonActionPerformed

